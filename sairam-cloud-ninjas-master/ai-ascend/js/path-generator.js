import { apiGet, apiPost } from "./services/api.js"

const statusEl = document.getElementById("path-status")
const outputEl = document.getElementById("path-output")
const generateBtn = document.getElementById("generate-path-btn")

function setStatus(text, isError = false){
  statusEl.textContent = text
  statusEl.className = isError ? "status error" : "status success"
}

function setBusy(busy){
  generateBtn.disabled = busy
  generateBtn.textContent = busy ? "Generating..." : generateBtn.dataset.label
}

function phaseMarkup(phase){
  const goals = Array.isArray(phase.goals) ? phase.goals : []
  const resources = Array.isArray(phase.resources) ? phase.resources : []
  return `
    <article class="roadmap-phase">
      <p class="meta">Week ${phase.week || "-"}</p>
      <h3>${phase.title || "Learning Phase"}</h3>
      <p class="meta"><strong>Goals:</strong> ${goals.join(" | ")}</p>
      <p class="meta"><strong>Resources:</strong> ${resources.join(" | ")}</p>
      <p class="meta"><strong>Deliverable:</strong> ${phase.deliverable || "Weekly progress checkpoint"}</p>
    </article>
  `
}

function renderPath(path, source = "ai"){
  if(!path){
    outputEl.innerHTML = `<p class="section-subtitle">No generated path yet.</p>`
    return
  }

  const sourceText = source === "fallback"
    ? "Generated from fallback planner (AI unavailable)."
    : `Generated by AI model (${source || "provider"}).`
  const phases = Array.isArray(path.phases) ? path.phases : []
  outputEl.innerHTML = `
    <p class="eyebrow">Personalized Roadmap</p>
    <h2>${path.title || `${path.careerGoal} Learning Plan`}</h2>
    <p class="section-subtitle">${path.summary || ""}</p>
    <p class="meta">Goal: ${path.careerGoal} | Level: ${path.currentLevel} | Hours/week: ${path.hoursPerWeek}</p>
    <p class="meta">Estimated timeline: ${path.timelineWeeks} weeks | Style: ${path.learningStyle}</p>
    <p class="meta">${sourceText}</p>
    <div class="roadmap-grid">
      ${phases.map(phaseMarkup).join("")}
    </div>
  `
}

async function loadLatest(){
  try{
    const data = await apiGet("/ai/path/latest")
    renderPath(data.path, "ai")
  }catch(_error){
    renderPath(null)
  }
}

async function generatePath(){
  const careerGoal = document.getElementById("career-goal").value.trim()
  const currentLevel = document.getElementById("current-level").value
  const hoursPerWeek = Number(document.getElementById("hours-per-week").value)
  const learningStyle = document.getElementById("learning-style").value

  if(!careerGoal){
    setStatus("Career goal is required.", true)
    return
  }
  if(Number.isNaN(hoursPerWeek) || hoursPerWeek < 1 || hoursPerWeek > 80){
    setStatus("Hours per week must be between 1 and 80.", true)
    return
  }

  try{
    setBusy(true)
    setStatus("")
    const data = await apiPost("/ai/path/generate", {
      careerGoal,
      currentLevel,
      hoursPerWeek,
      learningStyle
    })
    renderPath(data.path, data.source)
    setStatus("Personalized path generated and saved.")
  }catch(error){
    setStatus(error.message, true)
  }finally{
    setBusy(false)
  }
}

generateBtn.addEventListener("click", generatePath)
loadLatest()
